package com.bonrix.dggenraterset.DTO;

public class EngDTO {

	String Analogunit;
	String analogname;
	int analogioindex;
	String Analoginput;

	public String getAnalogunit() {
		return Analogunit;
	}

	public void setAnalogunit(String analogunit) {
		Analogunit = analogunit;
	}

	public String getAnalogname() {
		return analogname;
	}

	public void setAnalogname(String analogname) {
		this.analogname = analogname;
	}

	public int getAnalogioindex() {
		return analogioindex;
	}

	public void setAnalogioindex(int analogioindex) {
		this.analogioindex = analogioindex;
	}

	public String getAnaloginput() {
		return Analoginput;
	}

	public void setAnaloginput(String analoginput) {
		Analoginput = analoginput;
	}

}
